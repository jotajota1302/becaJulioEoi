    public static Pais fromCodigo(String codigo) {
        for (Pais pais : Pais.values()) {
            if (pais.codigo.equalsIgnoreCase(codigo)) {
                return pais;
            }
        }
        throw new IllegalArgumentException("No existe el país con el código: " + codigo);
    }

    public static Pais fromNombre(String nombre) {
        for (Pais pais : Pais.values()) {
            if (pais.nombre.equalsIgnoreCase(nombre)) {
                return pais;
            }
        }
        throw new IllegalArgumentException("No existe el país: " + nombre);
    }
